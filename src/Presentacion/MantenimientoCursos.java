    private Curso capturaCurso() {
        Curso curso = new Curso();
        curso.setNombreCurso(jTextFieldNombreCurso.getText());
        curso.setCosto(Double.parseDouble(jTextFieldCostoCurso.getText()));
        curso.setCupo(Integer.parseInt(jLabelCupo.getText()));
        curso.setProfesor1(jTextFieldProfesor1.getText());
        curso.setProfesor2(jTextFieldProfesor2.getText()); //aqui puede dar un error logico
        curso.setTipo(jCheckBoxLaboratorioCondicional.isSelected());
        curso.setSistemas(jCheckBoxSistemas.isSelected());
        curso.setSoftware(jCheckBoxSoftware.isSelected());

        return curso;
    }

