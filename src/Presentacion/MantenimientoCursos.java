    private TableModel fillTableModel() {
        try {
            List<Curso> listProductos = negocioCurso.consultarTodosLosCursos(); //hacer negocio Curso y las utilidades y manejo de datos curso y la interface 

            Object[] columnas = new Object[]{"Nombre Curso", "laboratorio", "Cupo", "Costo", "Profesor 1 ", "Profesor 2", "ingenieria Sistemas", "ingenieria software"};
            Object[][] datos = new Object[listProductos.size()][columnas.length];
            for (int i = 0; i < listProductos.size(); i++) {
                datos[i][0] = listProductos.get(i).getNombreCurso();
                datos[i][1] = listProductos.get(i).isTipo();
                datos[i][2] = listProductos.get(i).getCupo();
                datos[i][3] = listProductos.get(i).getCosto();
                datos[i][4] = listProductos.get(i).getProfesor1();
                datos[i][5] = listProductos.get(i).getProfesor2();
                datos[i][6] = listProductos.get(i).isSistemas();
                datos[i][7] = listProductos.get(i).isSoftware();

            }
            DefaultTableModel model = new DefaultTableModel(datos, columnas);
            return model;

        } catch (Exception ex) {
            Logger.getLogger(JFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        return null;
    }

    private boolean validarCurso() {
        boolean bandera = true;
        if (jTextFieldNombreCurso.getText().isEmpty()) {
            jTextAreaMensajes.setVisible(true);
            jTextAreaMensajes.setText("debe asignar un nombre al curso");
        }

        if (!isDouble(jTextFieldCostoCurso.getText()) || jTextFieldCostoCurso.getText().isEmpty()) {
            jTextAreaMensajes.setVisible(true);
            jTextAreaMensajes.setText("debe asignar un valor al curso en numeros");
        }

        if (jTextFieldProfesor1.getText().isEmpty()) {
            jTextAreaMensajes.setVisible(true);
            jTextAreaMensajes.setText("debe asignar un profesor al curso");
        }
        if (jCheckBoxLaboratorioCondicional.isSelected() == true && jTextFieldProfesor2.getText().isEmpty()) {
            jTextAreaMensajes.setVisible(true);
            jTextAreaMensajes.setText("por ser laboratorio debe asignar otro profesor al curso");

        }
        return bandera;
    }

    private boolean isDouble(String numero) {
        try {
            Double.parseDouble(numero);
            return true;
        } catch (Exception e) {
            return false;
        }
    }

